public class TypeCasting1 {
    public static void main(String[] args) {
        int a = 'c';
        char b = 99;

        int c = a+2;
        char d = 99-'c';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);


    }
}
